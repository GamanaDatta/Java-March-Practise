package StringConcept;

public class Stringimmutable {
	
public static void main(String[] args) {

		
		String s = "hello";
		System.out.println(s+"world");		
		System.out.println(s);
		
		System.out.println(s+"testing");
		
		
		String t = "selenium";
		t = t + "automation";
		System.out.println(t);
		
		String n = "N";
		n = "N"+"A";
		n = "N"+"A"+"V";
		n = "N"+"A"+"V"+"E";
		System.out.println(n);

		
	}

}
