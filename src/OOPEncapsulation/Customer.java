package OOPEncapsulation;

public class Customer {
	private String name;
	private int age;
	private double salary;
	private boolean isActive;
	
	
	
	public void setUserData(String name, int age, double salary, boolean isActive) {
		this.name = name;
		this.age = age;
		this.salary = salary;
		this.isActive = isActive;
	}

}
